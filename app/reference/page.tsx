export default function ReferencePage() {
  const [activeTab, setActiveTab] = useState("theory");
  const data = CONTENT[activeTab];

  return (
    <div className="animate-fade-in">
      <h1 className="text-2xl font-bold mb-6">📚 公式・解説まとめ</h1>

      {/* Tab Navigation */}
      <div className="flex flex-wrap gap-2 mb-6">
        {TABS.map((tab) => (
          <button
            key={tab.id}
            onClick={() => setActiveTab(tab.id)}
            className={`px-4 py-2 rounded-lg text-sm font-medium transition-colors ${
              activeTab === tab.id
                ? "bg-blue-600 text-white"
                : "bg-gray-800 text-gray-400 hover:text-white hover:bg-gray-700"
            }`}
          >
            {tab.name}
          </button>
        ))}
      </div>

      {/* Content */}
      {data && (
        <div>
          <h2 className="text-xl font-bold mb-4 text-blue-400">{data.title}</h2>
          <div className="space-y-4">
            {data.sections.map((section, i) => (
              <div
                key={i}
                className="bg-gray-800 rounded-xl p-5 border border-gray-700"
              >
                <h3 className="text-lg font-bold mb-3 text-yellow-400">
                  {section.heading}
                </h3>
                <pre className="text-gray-300 text-sm leading-relaxed whitespace-pre-wrap font-sans">
                  {section.content}
                </pre>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
}
